/**
 * Full crawl: pump.science compound tokens, Molecule ipNFTs, Research Hub papers + proposals.
 *
 * From repo root: node crawlers/full-crawl.mjs
 * From crawlers/:  node full-crawl.mjs
 *
 * Crawl-log (Arweave): set AGENT_WALLET and PATH_TO_KEYFILE in repo-root .env. Before crawls,
 * the latest on-chain JSON with tag doctype=crawllog is loaded; only entries marked reviewed
 * are skipped (unreviewed items are re-crawled after ephemeral restarts). After crawls, merged
 * crawl-log.json (v2 objects with optional reviewed field)
 * (researchhub, molecule, pump.science tickers) is uploaded via
 * python -m uploader --recipe crawl-log (tags: doctype, Crawl-Date, Content-Type); receipt at
 * crawlers/output/crawl-upload-receipt.json.
 * Pass --no-upload to skip the upload step (local testing).
 *
 * Manifests (papers_manifest.json, proposals/manifest.json) are always regenerated by the
 * Python crawlers; merge still includes them when present on disk so cumulative crawl-log
 * stays consistent.
 */
import { spawnSync } from "node:child_process";
import fsSync from "node:fs";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const crawlers = __dirname;
const repoRoot = path.join(crawlers, "..");

/** Load Claim-extractor/.env into process.env (does not override existing vars). */
function loadRepoRootEnv() {
  const envPath = path.join(repoRoot, ".env");
  if (!fsSync.existsSync(envPath)) return;
  const source = fsSync.readFileSync(envPath, "utf-8");
  for (const line of source.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    const key = trimmed.slice(0, eq).trim();
    if (!key || process.env[key] !== undefined) continue;
    let raw = trimmed.slice(eq + 1).trim();
    if (
      (raw.startsWith('"') && raw.endsWith('"')) ||
      (raw.startsWith("'") && raw.endsWith("'"))
    ) {
      raw = raw.slice(1, -1);
    }
    process.env[key] = raw;
  }
}

